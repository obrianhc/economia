    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            if(jTextField1.getText()!="" || jTextField2.getText()!="" || jTextField3.getText()!=""
                    || jTextField4.getText()!="" || jTextField5.getText()!="" || jTextField6.getText()!=""
                    || jTextField7.getText()!=""){
                this.a = Double.parseDouble(jTextField1.getText());
                this.b = Double.parseDouble(jTextField2.getText());
                this.c = Double.parseDouble(jTextField3.getText());
                this.d = Double.parseDouble(jTextField4.getText());
                this.e = Double.parseDouble(jTextField5.getText());
                this.f = Double.parseDouble(jTextField6.getText());
                this.m = Double.parseDouble(jTextField7.getText());
                this.flag = true;
                this.hide();
            } else {
                JOptionPane.showMessageDialog(null, "Deben llenarse todos los espacios en blanco");
            }
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error parseando, algun dato no es un n√∫mero");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

